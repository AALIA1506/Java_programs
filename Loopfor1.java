package Second;

public class Loopfor1 {

	public static void main(String[] args)
	{
		
      //multiple variables
		for(int i=0,j=10;i<j;i++,j--)
		{
			System.out.println("The value of i:"+i+" j: "+j);//i,j-will take as multi dimensional array i.e 2d array; nested for 
			System.out.println(i);
			System.out.println(j);
			
		}
	}

}
