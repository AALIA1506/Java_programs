package Second;

public class Advancedforloop {

	public static void main(String[] args) {
		// For each loop
		int [] numbers= {1,2,3,4,5};//[]-representation of array; size can be anything
		for(int num:numbers)//passing array to num variable so it fetches and prints one by one
			//, arraylist and using set we remove duplicates
		{
			System.out.println(num);
		}
		

	}

}
