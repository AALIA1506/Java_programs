package Second;

import java.util.Scanner;

public class Reverse_blast {
	
	public static void main(String[] args)
	{
		Scanner sc = new Scanner(System.in);
		int pr=sc.nextInt();
	
		while(pr>1)
		{
			System.out.println(pr);
			pr--;
		}
		System.out.println("The blast off");
	}

}
